function sortByYearAndId(arr) {
  return arr.sort((a, b) => {
    // 1.    ( )
    const yearDiff = Number(b.year) - Number(a.year);
    if (yearDiff !== 0) {
      return yearDiff;
    }

    // 2.   id
    const getIdNumber = (id) => {
      const match = id.match(/\d+/);
      return match ? Number(match[0]) : 0;
    };

    const idA = getIdNumber(a.id);
    const idB = getIdNumber(b.id);

    //  id  
    return idB - idA;
  });
}

